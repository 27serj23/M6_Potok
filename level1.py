"""
Модуль демонстрирует создание и запуск потоков с использованием стандартной библиотеки threading.
В каждом потоке выполняется задержка на одну секунду и выводится сообщение с названием потока.
"""
import threading
import time

def get_thread(thread_name):
    """
    Функция, выполняемая каждым потоком. Она ждет одну секунду,
    а затем выводит в консоль название данного потока.
    :param thread_name: Название текущего потока
    """
    time.sleep(1)  # Ждем одну секунду
    print(f"Поток {thread_name}")  # Выводим название потока

if __name__ == "__main__":
    # Создаём и запускаем 5 потоков с уникальными названиями
    for i in range(5):
        thread = threading.Thread(target=get_thread, args=(f"Поток_{i + 1}",))
        thread.start()
        thread.join()  # Дожидаемся завершения каждого потока


